def main(num):
    # If positive, print "The number is positive"
    
    if num  > 0:
        print ('The number is positive')
    # If negative, print "The number is negative"
    elif num < 0:
        print('The number is negative')
    # If zero, print "The number is zero"
    else: 
        print('The number is zero')
    pass


main(0)